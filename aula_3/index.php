<!--
 A diferença entre print e echo 
  Antigamente para verificar se houve algum erro na impressão de algum conteudo
  era usada a função print dessa forma: 
    print('conteudo');
  porém se acontecesse algum erro na impressão ela retonava o valor 0 se não ela retornava o valor 1 
  isso ajudava aos desenvolvedores da epoca a identifcar possiveis erros de impressão no código
  
  O acho não tem essa funcção então ele é um forma mais rapida de imprimir visto que ele não 
  precisa dar retorno, alem da linha impressa.
   
-->

   <?php print '<h1> comando print </h1> '; ?>
   <br>
   <?php echo  '<p> comando echo </p>'; ?>
    
